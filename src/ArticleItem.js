class ArticleItem{
    constructor(title,describe,href,read,like,comment,category,date){
        this.Title = title;
        this.Describe = describe;
        this.Href = href;
        this.Read = read;
        this.Like = like;
        this.Comment = comment;
        this.Category = category;
        this.Date = date;
    }
}

export default ArticleItem;